#include <iostream>

using namespace std;

/*
정수를 몇 개 저장할지 키보드로부터 개수를 입력받아(100보다 작은 개수) 정수 배열을 생성하고, 이곳에 1에서 100까지 범위의 정수를 랜덤하게 삽입.
배열에는 같은 수가 없도록 하고 배열을 출력하라.
*** 숫자를 렌덤하게 삽입;
*/

int inputArray[100] = { 0 };
char ch = 1;


int main() {
	std::cout << "정수를 몇개 저장하시겠습니까? : ";
	std::cin >> inputArray[100]; //정수를 몇게 저장할지 저장할 변수

	for (int i = 0; i <= inputArray[100]; ++i) {
		inputArray[i] = ++ch;
		std::cout << inputArray[i] << ' ';
	}
	std::cout << endl;
}